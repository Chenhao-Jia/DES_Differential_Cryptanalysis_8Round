def add_zero(x,num):
    h=len(x)
    if h<num:
        for i in range(num-h):
            x="0"+x
    return x


def xor(string,key,num):
    answer=""
    if len(string) != len(key) :
    	print("hahaha")
    for x in range(num):
        if string[x]==key[x] :
            answer+="0"
        else :
            answer+="1"
    return answer



